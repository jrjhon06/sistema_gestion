    private void jBtnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnModificarActionPerformed
        String idacceso;
        limpiarTextos();
        idacceso=JOptionPane.showInputDialog("IdAcceso de Usuario a Modificar : ");
        Acceso x;
        try {
            x = AccesoCN.getInstancia().buscarAcceso(idacceso);
            if(x!=null) {
                this.jTxtIdAcceso.setText(x.getIdacceso());
                this.jTxtNomUsuario.setText(x.getNomusuario());
                this.jTxtPassword.setText(x.getPassword());

                this.jCboPersona.setSelectedItem(x.getUsuario().getIdcarnet());
                
                reg=false;
                textos(true);
                controles(false);
            } else
            JOptionPane.showMessageDialog(null,"El IdCarnet de Usuario no existe");
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null,ex.getMessage());
        }
    }//GEN-LAST:event_jBtnModificarActionPerformed

