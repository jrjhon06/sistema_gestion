    private void jBtnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGrabarActionPerformed
        //        String idcarnet,apepat,apemat,nombres;
        Acceso acceso = new Acceso();
        if(this.jTxtIdAcceso.getText().compareTo("")!=0 && this.jTxtNomUsuario.getText().compareTo("")!=0
            && this.jTxtPassword.getText().compareTo("")!=0) {
            try {
                acceso.setIdacceso(this.jTxtIdAcceso.getText());
                
                //Obtengo Id
                Banda banda = (Banda) this.jCboPersona.getSelectedItem();
                String id = banda.getId();
                //Lleno td
                Usuario usu = UsuarioCN.getInstancia().buscarUsuario(id);
                acceso.setUsuario(usu);
                
                acceso.setNomusuario(this.jTxtNomUsuario.getText());
                acceso.setPassword(this.jTxtPassword.getText());

            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null,"aqui " + ex.getMessage());
            }

            if(reg) {
                try {
                    AccesoCN.getInstancia().insertar(acceso);
                    JOptionPane.showMessageDialog(null,"Dato registrado");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null,ex.getMessage());
                }

            } else {
                try {
                    AccesoCN.getInstancia().actualizar(acceso);
                    JOptionPane.showMessageDialog(null,"Dato modificado");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null,ex.getMessage());
                }
            }
            limpiarTextos();
            textos(false);
            controles(true);
        } else
        JOptionPane.showMessageDialog(null,"faltan llenar datos");
    }//GEN-LAST:event_jBtnGrabarActionPerformed

