    private void jBtnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnGrabarActionPerformed
//        String idcarnet,apepat,apemat,nombres;
        Usuario usuario = new Usuario();
        if(this.jTxtIdCarnet.getText().compareTo("")!=0 && this.jTxtApePat.getText().compareTo("")!=0 
                 && this.jTxtApeMat.getText().compareTo("")!=0  && this.jTxtNombres.getText().compareTo("")!=0) {
            try {
            usuario.setIdcarnet(this.jTxtIdCarnet.getText());
            usuario.setApepat(this.jTxtApePat.getText());
            usuario.setApemat(this.jTxtApeMat.getText());
            usuario.setNombres(this.jTxtNombres.getText());
            //Obtengo Id
            Banda banda = (Banda) this.jCboIdDocumento.getSelectedItem();
            String id = banda.getId();
            //Lleno td
            TipoDocumento td = TipoDocumentoCN.getInstancia().buscarTipoDocumento(id);
            usuario.setTipoDocumento(td);
            
            usuario.setNrodocumento(this.jTxtNroDocumento.getText());

            String fechanaci=new SimpleDateFormat("yyyy/MM/dd").format(jTxtFecNacim.getDate());
            usuario.setFechanaci(fechanaci);
            
            usuario.setDireccion(this.jTxtDireccion.getText());
            usuario.setTelefono(this.jTxtTelefono.getText());
            usuario.setCelular(this.jTxtCelular.getText());
            usuario.setEmail(this.jTxtEmail.getText());
            
            Banda banda1 = (Banda) this.jCboIdTipoUsuario.getSelectedItem();
            String idtu = banda1.getId();
            
            TipoUsuario tu = TipoUsuarioCN.getInstancia().buscarTipoUsuario(idtu);
            
            usuario.setTipoUsuario(tu);
            
            } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null,"aqui " + ex.getMessage());
            }
            
            if(reg) {
                try {
                    UsuarioCN.getInstancia().insertar(usuario);
                    JOptionPane.showMessageDialog(null,"Dato registrado");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null,ex.getMessage());
                }

            } else {
                try {
                    UsuarioCN.getInstancia().actualizar(usuario);
                    JOptionPane.showMessageDialog(null,"Dato modificado");
                } catch (SQLException ex) {
                    JOptionPane.showMessageDialog(null,ex.getMessage());
                }
            }
            limpiarTextos();
            textos(false);
            controles(true);
        } else
        JOptionPane.showMessageDialog(null,"faltan llenar datos");
    }//GEN-LAST:event_jBtnGrabarActionPerformed

