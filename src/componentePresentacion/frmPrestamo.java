    private void jBtnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnRegistrarActionPerformed
        
        if(this.jTxtIdPrestamo.getText().compareTo("")!=0 && this.jDtcFechaMaxima.getDate()!=null) {
            try{
                idprestamo=jTxtIdPrestamo.getText();
                String fechamax=new SimpleDateFormat("yyyy/MM/dd").format(jDtcFechaMaxima.getDate());
                fechamaxima=fechamax;
                
                //Lleno td
                Usuario usu = UsuarioCN.getInstancia().buscarUsuario(this.jTxtIdCarnet.getText());

                Prestamo pres=new Prestamo(idprestamo,usu,fechamaxima);
                
                int filas = modelo.getRowCount();

                for (int i = 0; i < filas; i++){
                    String idlibro = (String) modelo.getValueAt(i, 0);
//                    JOptionPane.showMessageDialog(null, (String) modelo.getValueAt(i, 0));
                    Libro li = LibroCN.getInstancia().buscarLibro(idlibro);
                    pres.registrarItemPrestamo(li,"");
                }

                PrestamoCN.getInstancia().insertar(pres);
                JOptionPane.showMessageDialog(null, "Prestamo registrado");

            }catch(SQLException ex){
                JOptionPane.showMessageDialog(null, ex.getMessage());
            }
        }
    }//GEN-LAST:event_jBtnRegistrarActionPerformed

