    private void jBtnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnAddActionPerformed

        if (this.jTxtIdLibro.getText().compareTo("")!=0 && this.jTxtTitulo.getText().compareTo("")!=0){
            if (verificaNoRepetirData(this.jTxtIdLibro.getText())) {
                Object[] fila = {this.jTxtIdLibro.getText(), this.jTxtTitulo.getText()};
                modelo.addRow(fila);
            }
            else
            JOptionPane.showMessageDialog(null, "IdLibro ya esta ingresado.");
        }
        else{
            JOptionPane.showMessageDialog(null, "Seleccione un Libro.");
        }
        jTxtIdLibro.setText("");
        jTxtTitulo.setText("");
    }//GEN-LAST:event_jBtnAddActionPerformed

    private void jBtnDelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnDelActionPerformed
        int i = Tabla.getSelectedRow();
        if (i != -1) {
            int rpta = JOptionPane.showConfirmDialog(null, "Esta seguro?", "Eliminar", JOptionPane.OK_CANCEL_OPTION, JOptionPane.QUESTION_MESSAGE);
            if (rpta == JOptionPane.OK_OPTION) {
                modelo.removeRow(i);
            }
        } else
        JOptionPane.showMessageDialog(null, "Seleccione un Autor", "Eliminar", 1);
    }//GEN-LAST:event_jBtnDelActionPerformed

